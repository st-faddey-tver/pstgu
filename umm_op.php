        <h3>Цели и задачи курса</h3>
        <p>Третья часть курса Основы Православия посвящена изучению Символа веры и богослужения Православной Церкви.</p>
        <p><strong>Цель третьей части курса:</strong> получение систематических  знаний об истории и смысле православного богослужения и основах православного вероучения в объеме, соответствующем уровню абитуриента ПСТГУ.</p>
        <p><strong>Основным пособием</strong> при изучении курса является книга «Основы Православия» протоиерея Фомы Хопко.</p>
        <p>Предполагается, что после завершения обучения на курсе, слушатель будет</p>
        <p><strong>знать и понимать:</strong></p>
