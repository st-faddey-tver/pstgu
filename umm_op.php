        <h2>Часть 2. Священная история Нового Завета</h2>
        <h3>Цели и задачи курса</h3>
        <p>Вторая часть курса "Основы Православия" посвящена изучению Священной истории Нового Завета и охватывает как евангельские события, так и начало истории апостольского века.</p>
        <p><strong>Цель второй части курса:</strong> получить целостное представление о содержании новозаветного Благовестия и осмыслить Священную историю Нового Завета в свете Священного Предания Православной Церкви.</p>
        <p>Данный курс Новозаветной истории проводится в рамках онлайн-курса «Основы Православия». Содержание программы и организация учебного процесса дают возможность желающим поступить в ПСТГУ подготовиться к поступлению и обучению в нашем Вузе. Всем, в чьи жизненные планы пока не входит получение систематического богословского образования, но стремящимся осознанно жить церковной жизнью, курс поможет глубже узнать основы православной веры.</p>
        <p><strong>Структура второй части курса</strong> задана двенадцатью великими праздниками Православной Церкви (так называемые «Двунадесятые праздники»), изучаемыми в соответствии с последовательностью евангельских событий. Такая особенность курса позволяет почувствовать органическую связь Священного Писания и Священного Предания и лучше понять смысл евангельских событий.</p>
        <p>Предполагается, что после завершения второй части курса «Основы Православия» слушатель</p>
        <p>а) будет <strong>знать и понимать</strong>:</p>
        <ul>
            <li>основное содержание новозаветного благовестия,</li>
            <li>событийную канву евангельских событий,</li>
            <li>историко-бытовой фон истории Нового Завета,</li>
            <li>связь новозаветной истории с богослужением Двунадесятых праздников;</li>
        </ul>
        <p>б) приобретет <strong>навыки</strong>:</p>
        <ul>
            <li>регулярного домашнего чтения слова Божия,</li>
            <li>вдумчивого чтения святоотеческих толкований и богословской литературы, </li>
            <li>участия в живых беседах на темы о вере и Церкви.</li>
